#!/usr/bin/env node
/**
 * Generate api/recipes.seed.generated.ts from src/data/recipes.json so Vercel's
 * serverless bundler inlines seed data (JSON/fs paths were unreliable).
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const src = join(root, 'src/data/recipes.json');
const dest = join(root, 'api/recipes.seed.generated.ts');

const data = JSON.parse(readFileSync(src, 'utf8'));
/** Slim seed for /api/og and /api/share — only fields those routes need (keeps bundle small). */
const slim = data.map(({ id, title, contributor, image, category }) => ({
  id,
  title,
  contributor,
  ...(image ? { image } : {}),
  ...(category ? { category } : {}),
}));
const banner = `/**
 * AUTO-GENERATED by scripts/sync-recipes-for-api.mjs — do not edit.
 * Source: src/data/recipes.json (slim fields for OG/share routes)
 */
`;
/** Embed JSON as JS via JSON.parse(JSON.stringify(raw)) so backslashes etc. cannot break parsing (e.g. \\) in prose). */
const rawJson = JSON.stringify(slim);
writeFileSync(dest, `${banner}export default JSON.parse(${JSON.stringify(rawJson)});\n`, 'utf8');
